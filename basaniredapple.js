// Task 1: Create Arrays

// An array of 5 grocery store items
const groceryItems = ["Apples", "Bread", "Milk", "Eggs", "Cheese"];

// An array of 3 numbers representing prices
const prices = [2.99, 1.49, 3.75];

// Task 2: Access and Log the Values

// 1. Log the entire array of grocery items
console.log("Grocery Items:", groceryItems);

// 2. Log the entire array of prices
console.log("Prices:", prices);

// 3. Log three individual grocery items
console.log("Item 1:", groceryItems[0]); // Apples
console.log("Item 2:", groceryItems[2]); // Milk
console.log("Item 3:", groceryItems[4]); // Cheese

// 4. Log two prices
console.log("Price 1:", prices[0]); // 2.99
console.log("Price 2:", prices[2]); // 3.75
